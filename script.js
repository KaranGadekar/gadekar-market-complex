  function normalizeLang(lang) {
    return lang === 'mr' || lang === 'hi' || lang === 'en' ? lang : 'en';
  }

  function applyFaqLanguage(lang) {
    var l = normalizeLang(lang);
    var dict = faqTranslations[l] || faqTranslations.en;
    var faqRoot = document.getElementById('faq');
    if (!faqRoot) return;

    var qNodes = faqRoot.querySelectorAll('[data-faq-q]');
    for (var i = 0; i < qNodes.length; i++) {
      var qKey = qNodes[i].getAttribute('data-faq-q');
      if (!qKey) continue;
      if (dict[qKey] == null) continue;
      qNodes[i].textContent = dict[qKey];
    }

    var aNodes = faqRoot.querySelectorAll('[data-faq-a]');
    for (var j = 0; j < aNodes.length; j++) {
      var aKey = aNodes[j].getAttribute('data-faq-a');
      if (!aKey) continue;
      if (dict[aKey] == null) continue;
      aNodes[j].textContent = dict[aKey];
    }
  }

  function applyLanguage(lang) {
    var l = normalizeLang(lang);
    var dict = translations[l] || translations.en;
    document.documentElement.setAttribute('lang', l);

    // Requirement: only the FAQ changes language.
    applyFaqLanguage(l);

    var nodes = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < nodes.length; i++) {
      var key = nodes[i].getAttribute('data-i18n');
      if (!key) continue;
      if (dict[key] == null) continue;
      nodes[i].textContent = dict[key];
    }

    var htmlNodes = document.querySelectorAll('[data-i18n-html]');
    for (var j = 0; j < htmlNodes.length; j++) {
      var htmlKey = htmlNodes[j].getAttribute('data-i18n-html');
      if (!htmlKey) continue;
      if (dict[htmlKey] == null) continue;
      htmlNodes[j].innerHTML = dict[htmlKey];
    }

    var buttons = document.querySelectorAll('.lang-btn[data-lang]');
    for (var b = 0; b < buttons.length; b++) {
      var btnLang = normalizeLang(buttons[b].getAttribute('data-lang'));
      var isActive = btnLang === l;
      buttons[b].classList.toggle('is-active', isActive);
      buttons[b].setAttribute('aria-pressed', isActive ? 'true' : 'false');
    }

    try { localStorage.setItem('lang', l); } catch (_) {}
  }

  // Init language
  var initialLang = 'en';
  try {
    var stored = localStorage.getItem('lang');
    if (stored) initialLang = normalizeLang(stored);
  } catch (_) {}
  applyLanguage(initialLang);

